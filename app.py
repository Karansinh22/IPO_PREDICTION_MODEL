def create_intelligence_summary(prediction, inputs):
    """Generates a heuristic AI summary/verdict based on prediction data."""
    gain = float(prediction.gain.replace('%', ''))
    risk = prediction.risk
    
    # Heuristic Logic for "Verdict"
    verdict = ""
    if gain > 40 and 'Low' in risk:
        verdict = f"Strong Alpha Signal. {prediction.ipo_name} displays exceptional demand-to-risk dynamics. Subscription patterns suggest a 'Blockbuster' listing."
    elif gain > 20:
        verdict = f"Growth Opportunity. The model predicts healthy listing gains. While the technical indicators are bullish, investors should monitor {risk} risk factors."
    elif gain > 5:
        verdict = f"Moderate Play. {prediction.ipo_name} metrics are stable but not explosive. A defensive strategy is recommended for this {risk} risk entry."
    else:
        verdict = f"Cautionary Outlook. Low margin of safety detected. Metrics align with historical underperformers in the current market cycle."

    # SWOT Analysis Generation
    swot = {
        "Strengths": [],
        "Weaknesses": [],
        "Opportunities": [],
        "Threats": []
    }
    
    # Heuristic SWOT assignment
    total_sub = inputs.get('Total', inputs.get('Subscription', 0))
    if total_sub > 50: swot["Strengths"].append(f"Massive Subscription Interest ({total_sub}x)")
    else: swot["Weaknesses"].append(f"Subdued Retail/HNI Appetite ({total_sub}x)")
    
    if gain > 25: swot["Opportunities"].append("Potential for sustained post-listing premium")
    
    if 'High' in risk: swot["Threats"].append("Significant valuation volatility")
    else: swot["Strengths"].append("Stable risk-adjusted fundamental profile")
    
    issue_size = inputs.get('Issue_Size(crores)', inputs.get('IPO_Size', 0))
    if issue_size > 2000: swot["Weaknesses"].append("Large supply may dampen immediate price discovery")
    
    return verdict, swot

